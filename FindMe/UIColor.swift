import Foundation
import UIKit

extension UIColor{
    
    static func customPinkColor() -> UIColor{
        return UIColor(red:0.87, green:0.22, blue:0.42, alpha:1.0)
    }
    
    static func customPinkDarkColor() -> UIColor{
        return UIColor(red:0.73, green:0.17, blue:0.35, alpha:1.0)
    }
    
}